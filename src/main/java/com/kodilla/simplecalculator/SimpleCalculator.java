package com.kodilla.simplecalculator;

import java.util.*;
import java.lang.*;
import java.io.*;

public class SimpleCalculator {
    public int addAToB(int a,int b) {
        return a+b;
    }
    public int substractAFromB(int a,int b) {
        return a-b;
    }
    public int multiplyAAndB(int a,int b) {
        return a*b;
    }
    public int divideAByB(int a,int b) {
        return a/b;
    }
    public int moduloAByB(int a,int b) {
        return a%b;
    }
}
